#include "cfrontera1d.h"

/* ------------------------------------------------------------------------------ */

cfrontera1d::cfrontera1d () {
    this->id = -1000; 
}

/* ------------------------------------------------------------------------------ */

cfrontera1d::cfrontera1d (int n) {
    this->id = n;
} 

/* ------------------------------------------------------------------------------ */

cfrontera1d::cfrontera1d (int n, cnodo1d *node) {
    id = n;
    nodo = node;   
}

/* ------------------------------------------------------------------------------ */

void cfrontera1d::print () {
    printf ("   %d:   %4d\n", this->id, this->nodo->id);
}

