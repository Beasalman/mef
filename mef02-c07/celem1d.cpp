#include "celem1d.h"

/* -------------------------------------------------------------------------------------- */

celem1d::celem1d () {

    this->id = -1000;
}

/* -------------------------------------------------------------------------------------- */

celem1d::celem1d (int n) {

    this->id = n;
}

/* -------------------------------------------------------------------------------------- */

celem1d::celem1d (int n, cnodo1d *n1, cnodo1d *n2) {

    this->id = n;
    this->nodos.push_back (n1);
    this->nodos.push_back (n2);
}

/* -------------------------------------------------------------------------------------- */

void celem1d::jacobiano () {

    this->xm = 0.5 * (this->nodos[1]->x + this->nodos[0]->x);
    this->h = this->nodos[1]->x - this->nodos[0]->x;
    this->jac = 0.5 * this->h;
}

/* -------------------------------------------------------------------------------------- */

void celem1d::print () {

    printf ("   %5d	Nodos (%4d, %4d)\n", this->id, this->nodos[0]->id, this->nodos[1]->id);
}

/* -------------------------------------------------------------------------------------- */

bool celem1d::contains (double y) {

    bool s = false;
    if (this->nodos[0]->x <= y && y <= this->nodos[1]->x) {
        s = true;
    }
    return s;
}

